a = 4
b = 10
# addition +
sum = a + b
print('Sum=', sum)

# Subtraction -
diff = b - a
print('Difference=', diff)

# Multiplication *
prod = a * b
print('Product=', prod)

# Division. /
div = b / a
print('Quoient=', div)

# To the power of ^  **
pow = b ** a
print('10^3=', pow)

# Reminder  %
rem = b % a
print('Reminder=', rem)

# Lowest integer quottent //
div1 = b // a
print('Quient=', div1)
